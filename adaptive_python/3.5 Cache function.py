import sys
from functools import lru_cache

def f(x): # this function for only a checking
    x = x ** 2
    return x


@lru_cache() # a solution using a decorator
def func(x):
    return f(x)


x = input() # Просто так. Можно и без него, но по условию нужен ввод
for i in sys.stdin:
    print(func(int(i)))

